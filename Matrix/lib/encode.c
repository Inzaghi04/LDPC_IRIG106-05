void shift_128(uint64_t *block) {
    uint64_t carry_to_high = (block[1] & 0x01ULL) << 63;
    uint64_t carry_to_low  = (block[0] & 0x01ULL) << 63;
    block[0] = (block[0] >> 1) | carry_to_high;
    block[1] = (block[1] >> 1) | carry_to_low;
}

void encode(const uint8_t *message, uint8_t *codeword)
{
    uint64_t parity[16];
    
    /* 1. Initialize parity accumulator to zero */
    memset(parity, 0, sizeof(parity));
    
    /* 2. Process each 128-bit circulant group */
    for (int group = 0; group < 8; group++) {
        uint64_t current_row[16];
        memcpy(current_row, G_SEEDS[group], sizeof(current_row));
        
        for (int row_idx = 0; row_idx < 128; row_idx++) {
            int bit_pos = group * 128 + row_idx;
            
            /* Conditional modulo-2 addition */
            if (message[bit_pos]) {
                for (int i = 0; i < 16; i++) {
                    parity[i] ^= current_row[i];
                }
            }
            
            /* Perform independent 128-bit circular shifts matching the hardware architecture */
            for (int block = 0; block < 8; block++) {
                shift_128(&current_row[block * 2]);
            }
        }
    }
    
    /* 3. Construct the systematic output codeword */
    for (int i = 0; i < 1024; i++) {
        codeword[i] = message[i];
    }
    
    /* Extract parity bits starting from the MSB down to LSB */
    for (int i = 0; i < 16; i++) {
        for (int bit = 0; bit < 64; bit++) {
            codeword[1024 + i * 64 + bit] = (uint8_t)((parity[i] >> (63 - bit)) & 0x01ULL);
        }
    }
}